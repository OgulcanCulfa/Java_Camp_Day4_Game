package day4_odev_game.Entities;


public class Campaign {
	public String campaignId;
	public int amountOfDiscountByHundred;
	
	public String getCampaignId() {
		return campaignId;
	}
	public void setCampaignId(String campaignId) {
		this.campaignId = campaignId;
	}
	public int getAmountOfDiscountByHundred() {
		return amountOfDiscountByHundred;
	}
	public void setAmountOfDiscountByHundred(int amountOfDiscountByHundred) {
		this.amountOfDiscountByHundred = amountOfDiscountByHundred;
	}
	
}
